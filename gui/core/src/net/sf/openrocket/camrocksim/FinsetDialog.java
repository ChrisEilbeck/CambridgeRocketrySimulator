	private void FinsetAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinsetAddButtonActionPerformed
		// TODO add your handling code here:
		boolean Goer = CheckValidity();
		if (Goer == true) {
			switch (FinsetFinNumComboBox.getSelectedIndex()) {
			case 0:
				FinNum = 3;
				break;
			case 1:
				FinNum = 4;
				break;
			}
			ReadOk = true;
			this.dispose();
		} else {
			JOptionPane.showMessageDialog(null, ("Something is wrong with the data that you entered"));
			;
		}
	}//GEN-LAST:event_FinsetAddButtonActionPerformed
	
