	private boolean CheckValidity() {
		boolean answer;
		Vector<javax.swing.JTextField> FieldsList = new Vector<javax.swing.JTextField>();
		FieldsList.add(FinsetRCText);
		FieldsList.add(FinsetTCText);
		FieldsList.add(FinsetSpanText);
		FieldsList.add(FinsetSweepText);
		FieldsList.add(FinsetBDText);
		FieldsList.add(FinsetThickText);
		FieldsList.add(FinsetMassText);
		FieldsList.add(FinsetPosText);
		
		TestUserTextInput TUI1 = new TestUserTextInput(FieldsList);
		answer = TUI1.TestDoubleList();
		return (answer);
	}
	
