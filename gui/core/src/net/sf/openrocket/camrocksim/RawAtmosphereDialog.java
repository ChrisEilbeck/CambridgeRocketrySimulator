	private boolean ReadTableData() {
		boolean temp = false;
		TestUserTextInput TUI = new TestUserTextInput(AtmosTable);
		
		Vector<Vector<Double>> Data = TUI.TestTable();
		
		Altitude = Data.elementAt(0);
		SetCalcButtons(Altitude);
		
		
		if (TUI.Valid) {
			
			Xwind = Data.elementAt(1);
			Ywind = Data.elementAt(2);
			Zwind = Data.elementAt(3);
			density = Data.elementAt(4);
			Temperature = Data.elementAt(5);
			temp = true;
		}
		return (temp);
	}
	
	private void SetCalcButtons(Vector<Double> vin) {
		if (vin.size() > 0) {
			CDensityButton.setEnabled(true);
			CTempButton.setEnabled(true);
		} else {
			CDensityButton.setEnabled(false);
			CTempButton.setEnabled(false);
			
		}
		
	}
	
	public boolean CheckValidity() {
		boolean goer = false;
		
		if (ReadTableData()) {
			goer = true;
			double tref = 0;
			for (double t : Altitude) {
				if (t < tref) {
					goer = false;
				}
				tref = t;
			}
			//TODO maybe add more checks later
			
		}
		return (goer);
	}
	
	public void FillFields(Vector<Double> v1, Vector<Double> v2, Vector<Double> v3, Vector<Double> v4, Vector<Double> v5, Vector<Double> v6, String s1) {
		Altitude = v1;
		Xwind = v2;
		Ywind = v3;
		Zwind = v4;
		density = v5;
		Temperature = v6;
		name = s1;
		
		WPnameText.setText(name);
		PopulateTable();
	}
	
	private void PopulateTable() {
		for (int i = 0; i < Altitude.size(); i++) {
			AtmosTable.getModel().setValueAt(Altitude.get(i), i, 0);
			AtmosTable.getModel().setValueAt(Xwind.get(i), i, 1);
			AtmosTable.getModel().setValueAt(Ywind.get(i), i, 2);
			AtmosTable.getModel().setValueAt(Zwind.get(i), i, 3);
			AtmosTable.getModel().setValueAt(density.get(i), i, 4);
			AtmosTable.getModel().setValueAt(Temperature.get(i), i, 5);
		}
		
	}
	
