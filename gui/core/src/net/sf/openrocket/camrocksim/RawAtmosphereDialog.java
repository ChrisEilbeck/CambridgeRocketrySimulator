	private void CDensityButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CDensityButtonActionPerformed
		density.clear();
		int i = 0;
		for (double Alt : Altitude) {
			double dense = -(0.0001 * Alt) + 1.2208;
			if (dense < 0) {
				dense = 0;
			}
			density.add(dense);
			AtmosTable.getModel().setValueAt(density.get(i), i, 4);
			i++;
		}
		
	}//GEN-LAST:event_CDensityButtonActionPerformed
	
	private void CTempButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CTempButtonActionPerformed
		
		Temperature.clear();
		double[][] LapseRate = new double[3][7];
		
		LapseRate[0][0] = 11000;
		LapseRate[1][0] = -0.0065;
		LapseRate[2][0] = 15;
		LapseRate[0][1] = 20000;
		LapseRate[1][1] = 0;
		LapseRate[2][1] = -56.5;
		LapseRate[0][2] = 32000;
		LapseRate[1][2] = 0.001;
		LapseRate[2][2] = -56.5;
		LapseRate[0][3] = 47000;
		LapseRate[1][3] = 0.0028;
		LapseRate[2][3] = -44.5;
		LapseRate[0][4] = 51000;
		LapseRate[1][4] = 0;
		LapseRate[2][4] = -2.5;
		LapseRate[0][5] = 71000;
		LapseRate[1][5] = -0.0028;
		LapseRate[2][5] = -2.5;
		LapseRate[0][6] = 84852;
		LapseRate[1][6] = -0.002;
		LapseRate[2][6] = -58.5;
		
		for (double Alt : Altitude) {
			if (Alt < LapseRate[0][0]) {
				Temperature.add(273 + LapseRate[2][0] + Alt * LapseRate[1][0]);
				continue;
			}
			for (int i = 1; i < 7; i++) {
				if (Alt < LapseRate[0][i]) {
					Temperature.add(273 + LapseRate[2][i] + (Alt - LapseRate[0][i - 1]) * LapseRate[1][i]);
					break;
				} else {
					Temperature.add(273 + LapseRate[2][6]);
				}
			}
			
		}
		for (int i = 0; i < Altitude.size(); i++) {
			AtmosTable.getModel().setValueAt(Temperature.get(i), i, 5);
		}
	}//GEN-LAST:event_CTempButtonActionPerformed
	
