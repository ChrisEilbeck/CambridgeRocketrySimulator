	private void exportCSV() {
		// exports the trajectories to a CSV file, to be selected by the users
		
		
		// get simulation output
		File thisSimulationOutputF = new File(SystemInfo.getUserApplicationDirectory(), 
				SystemInfo.DATA_FOLDER + File.separator + SystemInfo.OUTPUT_SIMULATION);
		
		RWsimOutXML thisSimulationOutput = new RWsimOutXML( thisSimulationOutputF.toString() );
		
		// fill structure from XML
		thisSimulationOutput.ReadXMLtoSimOdata();
		
		Vector<SimulationOutputData> DataList = thisSimulationOutput.getDataList();
		
		// save as
		
		File fileSimulationOutputCSV = new File(SystemInfo.getUserApplicationDirectory(), (SystemInfo.DATA_FOLDER + File.separator + "output.csv"));
		
		
		String FileName = null;
		
		JFileChooser fc = new JFileChooser();
		fc.setCurrentDirectory(fileSimulationOutputCSV);
		int RetVal = fc.showSaveDialog(null);
		if (RetVal == fc.APPROVE_OPTION) {
			FileName = fc.getSelectedFile().getPath();
		}
		
		
		if (FileName != null)
		{
			
			try {
				
				// success
				FileWriter thisFileWriter = new FileWriter( FileName );
				
				// formatting output
				NumberFormat fd = new DecimalFormat("#0.0000"); 
				
				// headers
				thisFileWriter.append( "INDEX,ID_STR,ID_STAGE_STR,TIME_SECONDS_DOUBLE,EASTINGS_METERS_DOUBLE,NORTHINGS_METERS_DOUBLE,ALTITUDE_METERS_DOUBLE,DISTANCE_METERS_DOUBLE,EVENT_INT\n" );
				
				int iTraj = 1;
				
				// TODO add EVENT
				
				for (SimulationOutputData thisData : DataList)
				{
					
					// get event timings
					Vector<Double> vecEvents = thisData.mEvents;
					
					// obtain size
					int sizeVec = thisData.mTime.get(0).size();
					
					double dist1 = 0; // absolute distance covered
					
					double east0 = 0, nort0 = 0, alti0 = 0; // store old position
					
					for (int i = 0; i < sizeVec; i++) {
						
						// new values
						double time1 = thisData.mTime.get(0).get(i);
						double east1 = thisData.mPosition.get(0).get(i);
						double nort1 = thisData.mPosition.get(1).get(i);
						double alti1 = thisData.mPosition.get(2).get(i);
						
						// absolute distance covered in one step
						double ddist = 	Math.sqrt(	Math.pow(east1 - east0, 2) +
													Math.pow(nort1 - nort0, 2) +
													Math.pow(alti1 - alti0, 2) );
						
						dist1 += ddist; // add difference to total
						
						String stag1 = thisData.StageName;
						String id1 = thisData.ID;
						
						// identify event
						int iEvent = 0;
						while ( time1 > vecEvents.get(iEvent) ) {
							iEvent++;
						}
						
						thisFileWriter.append( 
										Integer.toString(i) + "," + 
										id1 + "," + 
										stag1 + "," + 
										fd.format(time1) + "," + 
										fd.format(east1) + "," + 
										fd.format(nort1) + "," + 
										fd.format(alti1) + "," + 
										fd.format(dist1) + "," + 
										Integer.toString(iEvent) + "\n");
						
						
						
					}
					
					iTraj++; // next trajectory
					
				}
				
				thisFileWriter.flush();
				thisFileWriter.close();
				
			}
			catch (Exception e1) {
				JOptionPane.showMessageDialog(null, ("Something is wrong with the data that you entered"));
				FileName = null; // set back to null, don't want to use it.
			}
		}
				
	}
	
	private class RunningDialog extends JDialog {
		/**
		 *  cancel button
		 */
		private static final long serialVersionUID = 1L;
		private JProgressBar progress = new JProgressBar();
		private boolean isDone = false;

		private RunningDialog() {
			super(null, "Running Simulation", ModalityType.APPLICATION_MODAL);
			
			JPanel panel = new JPanel(new MigLayout("fill"));
			panel.add(new JLabel("Running .."), "wrap para");
			
			progress.setIndeterminate(false);
			progress.setMinimum(0);
			
			panel.add(progress, "growx");
			
			this.add(panel);
			this.pack();
			// this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
			this.setLocationByPlatform(true);
			GUIUtil.setWindowIcons(this);
		}
		
		public void adjustProgress(int currentValue, int totalValue) {
			
			progress.setMaximum(totalValue);
			progress.setValue(currentValue);
			
			if (totalValue == currentValue) {
				isDone = true;
			}
			
		}
		
		public boolean isDone() {
			return this.isDone;
		}
		
	}

	
